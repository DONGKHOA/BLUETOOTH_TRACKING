void
DEV_RS485_SendData (uart_port_num_t e_uart_port,
                    const uint8_t  *u8_data,
                    size_t          u32_len)
{
  BSP_uartSendData(e_uart_port, u8_data, u32_len);
  BSP_uartWaitTXDone(
      e_uart_port,
      pdMS_TO_TICKS(100)); // Test thử xuống thấp nhất (us nếu được)
}

